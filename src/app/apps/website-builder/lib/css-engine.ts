/* ========== Google Fonts URL ========== */

export function getGoogleFontsUrl(theme: SiteTheme): string {
  const fonts = new Set<string>();
  const addFont = (name: string) => {
    const map: Record<string, string> = {
      'Inter': 'Inter:wght@300;400;500;600;700;800;900',
      'DM Sans': 'DM+Sans:wght@400;500;600;700',
      'Outfit': 'Outfit:wght@300;400;500;600;700;800',
      'Plus Jakarta Sans': 'Plus+Jakarta+Sans:wght@400;500;600;700;800',
      'Poppins': 'Poppins:wght@300;400;500;600;700',
      'Roboto': 'Roboto:wght@300;400;500;700',
      'Open Sans': 'Open+Sans:wght@300;400;600;700',
      'Montserrat': 'Montserrat:wght@400;500;600;700;800',
      'Lato': 'Lato:wght@300;400;700',
      'Space Grotesk': 'Space+Grotesk:wght@400;500;600;700',
      'Playfair Display': 'Playfair+Display:wght@400;500;600;700;800',
      'Merriweather': 'Merriweather:wght@300;400;700',
      'Lora': 'Lora:wght@400;500;600;700',
      'JetBrains Mono': 'JetBrains+Mono:wght@400;500;700',
      'Fira Code': 'Fira+Code:wght@400;500;700',
    };
    if (map[name]) fonts.add(map[name]);
  };
  addFont(theme.fonts.heading);
  addFont(theme.fonts.body);
  if (fonts.size === 0) return '';
  return `https://fonts.googleapis.com/css2?${[...fonts].map(f => `family=${f}`).join('&')}&display=swap`;
}

/* ========== Collect All Hover Styles from a Page ========== */

export function collectHoverCSS(sections: SectionNode[]): string {
  const rules: string[] = [];
  const walk = (elements: ElementNode[]) => {
    for (const el of elements) {
      const hover = generateElementHoverCSS(el.id, el.hoverStyles);
      if (hover) rules.push(hover);
      if (el.children.length > 0) walk(el.children);
    }
  };
  for (const sec of sections) {
    for (const row of sec.rows) {
      for (const col of row.columns) {
        walk(col.elements);
      }
    }
  }
  return rules.join('\n');
}
