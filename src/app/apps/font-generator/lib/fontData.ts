/* ─── Build character-level lookup maps ──────────────────────────────────── */

export interface FontStyle {
  name: string;
  category: string;
  preview: string;
  mapping: Record<string, string>;
}

function buildMapping(def: FontStyleDef): Record<string, string> {
  const map: Record<string, string> = {};
  if (def.uppercase) {
    const chars = [...def.uppercase];
    [...UPPER].forEach((c, i) => { if (chars[i]) map[c] = chars[i]; });
  }
  if (def.lowercase) {
    const chars = [...def.lowercase];
    [...LOWER].forEach((c, i) => { if (chars[i]) map[c] = chars[i]; });
  }
  if (def.digits) {
    const chars = [...def.digits];
    [...DIGITS].forEach((c, i) => { if (chars[i]) map[c] = chars[i]; });
  }
  return map;
}

export const FONT_STYLES: FontStyle[] = RAW_FONTS.map((def) => ({
  name: def.name,
  category: def.category,
  preview: def.preview,
  mapping: buildMapping(def),
}));

export const CATEGORIES = Array.from(new Set(FONT_STYLES.map((f) => f.category)));

/** Convert a string using a font mapping, passing unmapped characters through. */
export function convertText(text: string, mapping: Record<string, string>): string {
  return [...text].map((c) => mapping[c] ?? c).join("");
}
